<p>If you’ve been looking through Google’s directory of <a class="red" href="https://www.google.com/fonts">Web Fonts</a> in search for fonts to use in your next project, you might have felt your inspiration quickly drain. It can be difficult to imagine how a font would look in use when you’re left with a long list of black text on white.</p>

<p>I built <a class="red" href="/typesource/">Typesource</a> to make it easier to find and match Google Web Fonts. I’ve carefully crafted each example, exploring different styles, combinations, and color schemes in every composition. In addition to the feed of inspiration, the HTML/CSS is available for all examples, so you can copy and paste the code into your project and quickly get started with your web type compositions.</p>

